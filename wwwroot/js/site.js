function readUrl(){
    var url = $('#urlInput').val();
    if (isUrlValid(url)){
        $.ajax({
            type: "GET",
            url: "../api/values",
            data: { 
                'url': url
            }, 
            contentType: "application/json; charset=utf-8",
            dataType: "json",
            success: successFunc,
            error: errorFunc
        });

        function successFunc(data) {     
            jQuery.each(data, function(i, val) {
                $("#results").append("<li>the number of occurrences of the word: " + val.Key + " is " + val.Value);
              });
        }

        function errorFunc() {
            alert('There is not a website in that URL');
        }
    }
    else{
        alert("Please insert a valid URL in the format http://www.example.com");
    }
}
